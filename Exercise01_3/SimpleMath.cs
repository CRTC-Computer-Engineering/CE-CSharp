namespace Exercise01_3
{
    public class SimpleMath
    {
        public static int SumTwoNumbers(int one, int two)
        // Takes two numbers and sums them
        {
            return (one + two); // Returns the result
        }

        public static double DivideTwoNumbers(double numerator, double denominator)
        // Takes two numbers and divides them
        {
            return (numerator / denominator); // Returns the result
        }
    }
